package com.games.fun.fun_games.enums;

/**
 * The Role enum represents the different roles that a user can have in the system.
 */
public enum Role {
    /**
     * The ROLE_USER represents a regular user role.
     */
    ROLE_USER,

    /**
     * The ROLE_ADMIN represents an administrator role.
     */
    ROLE_ADMIN
}